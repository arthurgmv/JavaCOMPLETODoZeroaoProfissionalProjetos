public class ConvNumToString {
    public static void main(String[] args) {
        Integer num1 = 1000;
        System.out.println("The value is: " + num1.toString());
        System.out.println("The length is: " + num1.toString().length());

        System.out.println("+=+=+=+=+=+=+=+=+=+=+=+=+");

        int num2 = 1000;
        System.out.println("The value is: " + Integer.toString(num2));
        System.out.println("The length is: " + Integer.toString(num2).length());

    }
}
